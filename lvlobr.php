                <summary>На бюджетные места</summary>
                <p>Прием проводится на первый курс на конкурсной основе по личному заявлению абитуриента, имеющего аттестат 11 классов или диплом о среднем профессиональном образовании. Диплом о высшем образовании — для поступления в магистратуру.
                    В рамках бюджетных мест выделяется квота приема в размере 10% от бюджетных мест по каждому направлению бакалавриата и специалитета на обучение для:
                    — детей-инвалидов, инвалидов I и II групп, инвалидов с детства, инвалидов вследствие военной травмы или заболевания, полученных в период прохождения военной службы, (далее — особая квота) при условии успешной сдачи вступительных испытаний;
                    — детей-сирот и детей, оставшихся без попечения родителей, а также лиц из числа детей-сирот и детей, оставшихся без попечения родителей, и ветеранов боевых действий из числа лиц, указанных в подпунктах 1–4 пункта 1 статьи 3 Федерального закона от 12 января 1995 года N 5-ФЗ «О ветеранах», при условии успешной сдачи вступительных испытаний.
                    
                    Проводятся отдельные конкурсы, формируются отдельные списки поступающих:
                    — на места в пределах особой квоты;
                    — основные места (общий конкурс);
                    — отдельно по направлениям бакалавриата и специалитета.
                    
                    Абитуриент имеет право подать заявления не более чем в 5 вузов.
                    Абитуриент имеет право одновременно подать заявления не более чем на 3 направления подготовки.
                    Абитуриент имеет право одновременно подать заявления на места в пределах особой квоты, основные места.
                    Абитуриент имеет право одновременно подать заявления на бюджетные места и на платные места.
                    По результатам приема документов и вступительных испытаний Университет формирует отдельный список поступающих по каждому конкурсу.
                    Для зачисления поступающий подает заявление о согласии на зачисление, к которому прилагается оригинал документа об образовании.
                    В заявлении о согласии на зачисление указываются условия поступления и основание приема по одному конкретному конкурсу, в соответствии с результатами которого поступающий хочет быть зачисленным. Поступающий может по своему усмотрению подать заявление о согласии на зачисление не более 2-х раз.
                    Согласие на зачисление заверяется подписью поступающего и подается в Университет не позднее дня завершения приема заявлений о согласии на зачисление. В день завершения приема заявлений о согласии на зачисление указанное заявление подается в Университет не позднее 18:00 часов по местному времени.
                    Зачислению подлежат поступающие, подавшие заявление о согласии на зачисление и оригинал аттестата. Зачисление проводится в соответствии с ранжированным списком до заполнения установленного количества мест.
                    Абитуриенты, которые были включены в списки поступающих по каждому отдельному конкурсу, но не представили в установленный срок (отозвали) заявление о согласии на зачисление и (или) оригинал документа об образовании, к зачислению не рассматриваются.</p>
