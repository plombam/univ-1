                    Абитуриент имеет право одновременно подать заявления не более чем на 3 направления подготовки, в рамках одного направления на разные формы обучения.
                    Абитуриент имеет право одновременно подать заявления на бюджетные места и на платные места.
                    По результатам приема документов и вступительных испытаний Университет формирует отдельный список поступающих по каждому конкурсу.
                    Для зачисления поступающий подает заявление о согласии на зачисление, к которому прилагается оригинал документа об образовании. В заявлении о согласии на зачисление указываются условия поступления и основание приема по одному конкретному конкурсу, в соответствии с результатами которого поступающий хочет быть зачисленным.
                    Зачисление проводится при:
                    — успешной сдаче вступительных испытаний;
                    — заключенном договоре об обучении;
                    — оплате обучения;
                    — наличии оригинала документа об образовании;
                    — наличии согласия о зачислении.
                    Если абитуриенту нет 18 лет, то договор об обучении заключается в присутствии одного из родителей (опекунов).
                    Абитуриенты, включенные в список лиц, рекомендованных к зачислению на платные места, не представившие в установленные сроки (отозвавшие) документ об образовании и согласие на зачисление, не заключившие (расторгнувшие) договор об обучении и (или) не оплатившие обучение за соответствующий период, а также не представившие квитанцию (платежное поручение) об оплате обучения до конца рабочего дня приемной комиссии рассматриваются как отказавшиеся от зачисления.</p>
